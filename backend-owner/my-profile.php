        protected function DoBeforeCreate()
        {
            $selectQuery = 'SELECT Ac.*, Aw.firstname, Aw.lastname  
            FROM `account` Ac 
            LEFT JOIN `owner` Aw ON Ac.account_id = Aw.account_id';
            $insertQuery = array();
            $updateQuery = array('INSERT INTO `owner` (`firstname`,`lastname`, account_id) VALUES (:firstname,:lastname, :account_id) 
              ON DUPLICATE KEY UPDATE `firstname` = :firstname , `lastname` = :lastname, account_id = :account_id', 
            'UPDATE `account` SET  
            `email` = :email   
            WHERE `account`.account_id = :account_id');
            $deleteQuery = array();
            $this->dataset = new QueryDataset(
              MySqlIConnectionFactory::getInstance(), 
              GetConnectionOptions(),
              $selectQuery, $insertQuery, $updateQuery, $deleteQuery, 'Query-Profile');
            $field = new StringField('account_id');
            $this->dataset->AddField($field, true);
            $field = new StringField('login');
            $this->dataset->AddField($field, false);
            $field = new StringField('password');
            $this->dataset->AddField($field, false);
            $field = new StringField('email');
            $this->dataset->AddField($field, false);
            $field = new StringField('role');
            $this->dataset->AddField($field, false);
            $field = new StringField('token');
            $this->dataset->AddField($field, false);
            $field = new StringField('status');
            $this->dataset->AddField($field, false);
            $field = new StringField('firstname');
            $this->dataset->AddField($field, false);
            $field = new StringField('lastname');
            $this->dataset->AddField($field, false);
            $this->dataset->AddCustomCondition(EnvVariablesUtils::EvaluateVariableTemplate($this->GetColumnVariableContainer(), 'account_id = %CURRENT_USER_ID%'));
        }
    
